// !IMPORTANT createAsyncThunk takes to arguments , 1) the action name

export const fetchAddress = createAsyncThunk(
  "user/fetchAddress",
  async function () {
    // 1) We get the user's geolocation position
    const positionObj = await getPosition();
    const position = {
      latitude: positionObj.coords.latitude,
      longitude: positionObj.coords.longitude,
    };

    // 2) Then we use a reverse geocoding API to get a description of the user's address, so we can display it the order form, so that the user can correct it if wrong
    const addressObj = await getAddress(position);
    const address = `${addressObj?.locality}, ${addressObj?.city} ${addressObj?.postcode}, ${addressObj?.countryName}`;

    // 3) Then we return an object with the data that we are interested in

    // !IMPORTANT  payload of the FULFILLED state look down below in the thunk function .

    return { position, address };
  }
);

const initialState = {
  username: "",
  status: "idle",
  position: {},
  address: "",
  error: "",
};

const userSlice = createSlice({
  name: "user",
  initialState,
  reducers: {
    updateName(state, action) {
      state.username = action.payload;
    },
  },

  // this is how to use thunks in redux toolkit , here is a link to understand more . https://redux-toolkit.js.org/api/createAsyncThunk .

  extraReducers: (builder) =>
    builder
      .addCase(fetchAddress.pending, (state, action) => {
        state.status = "loading";
      })
      .addCase(fetchAddress.fulfilled, (state, action) => {
        // NOTE this is the same comment from the fucntion above, just to make sure you are not lsot, the retruns of the funcion above is the paylaod of the thunk function . aslo we are making cases of different scenarios .

        state.position = action.payload.position;
        state.address = action.payload.address;
        state.status = "idle";
      })
      .addCase(fetchAddress.rejected, (state, action) => {
        state.status = "error";
        state.error = `there was a problem getting your address. Make sure to fill this field!`;
      }),
});

export const { updateName } = userSlice.actions;

export default userSlice.reducer;
